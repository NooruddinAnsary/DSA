package JAVA;

public class RealationOperators {
    public static void main(String arg[]){
        int a = 10;
        int b = 6;
        int c =10;
        System.out.println(a==b);
        System.out.println(a!=c);
        System.out.println(a==c);

        System.out.println(a>b);
        System.out.println(a>=c);
        System.out.println(a<b);
    }
}
